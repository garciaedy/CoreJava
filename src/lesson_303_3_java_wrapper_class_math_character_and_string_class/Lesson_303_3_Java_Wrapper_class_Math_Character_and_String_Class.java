package lesson_303_3_java_wrapper_class_math_character_and_string_class;



public class Lesson_303_3_Java_Wrapper_class_Math_Character_and_String_Class {
    public static void main(String[] args){
        String sample = "    Java world    ";
        System.out.println(sample.length());
        System.out.println(sample.charAt(2));
        System.out.println(sample.substring(1,6));
        System.out.println(sample.contains("o"));
        System.out.println(sample.replace("a", "u"));
        System.out.println(sample.toUpperCase());
        System.out.println(sample.toLowerCase());
        System.out.println(sample.trim());
        System.out.println(sample.indexOf('a'));
        System.out.println(sample.lastIndexOf('a'));
        System.out.println(sample.isEmpty());
        System.out.println(sample.isBlank());











    }
}
